def create_shap_waterfall(sample_idx: int = 0, n_top: int = 12) -> go.Figure:
    """Waterfall chart showing which features pushed the prediction toward/away from slip."""
    if not _xai_loaded or _shap_values is None:
        return _placeholder_fig("Run compute_xai.py to enable SHAP explanations")

    sv    = _shap_values[sample_idx]           # (102,)
    names = [_FEATURE_LABELS.get(n, n) for n in _shap_feat_names]

    # Pick top-n by |SHAP value|
    order  = np.argsort(np.abs(sv))[::-1][:n_top]
    # Reverse so largest bar is at top
    order  = order[::-1]
    sv_top = sv[order]
    nm_top = [names[i] for i in order]

    colors = ["#dc2626" if v > 0 else "#0284c7" for v in sv_top]
    texts  = [f"+{v:.3f}" if v > 0 else f"{v:.3f}" for v in sv_top]

    fig = go.Figure(go.Bar(
        x=sv_top,
        y=nm_top,
        orientation="h",
        marker_color=colors,
        text=texts,
        textposition="outside",
        textfont=dict(color="rgba(15,23,42,0.65)", size=9),
        hovertemplate=(
            "<b>%{y}</b><br>"
            "SHAP contribution: %{x:.4f}<br>"
            "<i>Positive = pushes toward SLIP<br>Negative = pushes toward STABLE</i>"
            "<extra></extra>"
        ),
    ))

    # Base-value reference line
    fig.add_vline(
        x=0,
        line_dash="solid",
        line_color="rgba(15,23,42,0.2)",
        line_width=1,
    )

    fig.update_layout(
        template="none",
        plot_bgcolor="rgba(0,0,0,0)",
        paper_bgcolor="rgba(0,0,0,0)",
        margin=dict(l=10, r=65, t=10, b=35),
        xaxis=dict(
            title=dict(
                text="← Pushes toward STABLE  |  Pushes toward SLIP →",
                font=dict(size=10, color="rgba(15,23,42,0.45)"),
            ),
            gridcolor="rgba(15,23,42,0.05)",
            zeroline=True,
            zerolinecolor="rgba(15,23,42,0.25)",
            zerolinewidth=1,
        ),
        yaxis=dict(
            gridcolor="rgba(0,0,0,0)",
            tickfont=dict(size=10),
        ),
        bargap=0.3,
        height=380,
    )
    return fig


def _shap_auto_caption(sample_idx: int = 0) -> str:
    """Generate a plain-English caption for the SHAP chart."""
    if not _xai_loaded or _shap_values is None:
        return ""
    sv    = _shap_values[sample_idx]
    names = [_FEATURE_LABELS.get(n, n) for n in _shap_feat_names]
    top_i = int(np.argmax(np.abs(sv)))
    direction = "increased" if sv[top_i] > 0 else "reduced"
    meta  = _xai_meta.get("samples", [{}] * (sample_idx + 1))[sample_idx]
    prob  = meta.get("rf_prob", 0.0)
    return (
        f"The model predicted a {prob:.0%} slip probability for this grasp. "
        f"The strongest driver was \"{names[top_i]}\", which {direction} slip risk "
        f"by {abs(sv[top_i]):.3f} units above the baseline rate of "
        f"{_shap_base:.0%}."
    )


