def create_taxel_heatmaps(sample_idx: int = 0) -> go.Figure:
    """3-panel heatmap — one per finger — coloured by CNN IG attribution."""
    if not _xai_loaded or _ig_cnn is None:
        return _placeholder_fig("Run compute_xai.py to enable attribution maps")

    ig    = _ig_cnn[sample_idx]   # (3, 4, 6)
    vmax  = float(np.abs(ig).max()) or 1.0

    fig = make_subplots(
        rows=1, cols=3,
        subplot_titles=[_FINGER_LABELS[i] for i in range(3)],
        horizontal_spacing=0.08,
    )

    finger_tags = ["ff", "mf", "th"]

    for col_idx in range(3):
        grid     = ig[col_idx]          # (4, 6)
        tag      = finger_tags[col_idx]
        cell_txt = [[
            f"taxel {r*6+c+1}<br>{grid[r, c]:.3f}"
            for c in range(6)] for r in range(4)
        ]

        fig.add_trace(go.Heatmap(
            z=grid,
            zmin=-vmax, zmax=vmax,
            colorscale=[
                [0.0, "#0284c7"],   # strong negative = stable
                [0.5, "#1e293b"],   # neutral
                [1.0, "#dc2626"],   # strong positive = slip
            ],
            text=cell_txt,
            hovertemplate="%{text}<extra></extra>",
            showscale=(col_idx == 2),
            colorbar=dict(
                title=dict(text="IG attr.", font=dict(size=9)),
                thickness=10,
                len=0.8,
                tickfont=dict(size=8),
            ) if col_idx == 2 else None,
            xgap=2, ygap=2,
        ), row=1, col=col_idx + 1)

        fig.update_xaxes(
            tickvals=list(range(6)),
            ticktext=[f"C{i+1}" for i in range(6)],
            tickfont=dict(size=8),
            row=1, col=col_idx + 1,
        )
        fig.update_yaxes(
            tickvals=list(range(4)),
            ticktext=[f"R{i+1}" for i in range(4)],
            tickfont=dict(size=8),
            autorange="reversed",
            row=1, col=col_idx + 1,
        )

    fig.update_layout(
        template="none",
        plot_bgcolor="rgba(0,0,0,0)",
        paper_bgcolor="rgba(0,0,0,0)",
        margin=dict(l=10, r=60, t=35, b=10),
        height=240,
        font=dict(color="white"),
    )
    # Style subplot titles
    for ann in fig.layout.annotations:
        ann.update(font=dict(size=11, color="rgba(15,23,42,0.75)"))

    return fig


def _ig_auto_caption(sample_idx: int = 0) -> str:
    if not _xai_loaded or _ig_cnn is None:
        return ""
    ig    = _ig_cnn[sample_idx]          # (3, 4, 6)
    finger_totals = np.abs(ig).sum(axis=(1, 2))
    dom_finger    = int(np.argmax(finger_totals))
    dom_grid      = ig[dom_finger]
    row, col      = np.unravel_index(np.argmax(np.abs(dom_grid)), dom_grid.shape)
    taxel_num     = row * 6 + col + 1
    direction     = "toward slip" if dom_grid[row, col] > 0 else "toward stable"
    return (
        f"The CNN traced the decision primarily to the "
        f"{_FINGER_LABELS[dom_finger].lower()} (taxel {taxel_num}, row {row+1} col {col+1}). "
        f"Red cells push {direction}; blue cells act as stabilising signals. "
        f"Inspect highlighted taxels for surface wear or contamination."
    )




